    	//rotate by pi about z axis s.t. telescope is in front of shower rather than behind
		/*vector<double> simPos = {-1*(0-ePos[0]),
			-1*(0-ePos[1]),
			Constant::REarth*1000+readConf->fTelAltitude-ePos[2]};*/
		std::vector<double> simPos = {ePos[0],
			ePos[1],
			Constant::REarth*1000+readConf->fTelAltitude-ePos[2]}; //position of telescope for CHASM coordinates (relative to shower emergence point)
		double chasmZN = showerZN + eTheta;

		for(int j = 0; j<energyFractions.size(); j++){
			energyProbability.push_back(energyFractionsLUT->Interpolate(TMath::Pi()/2 - chasmZN,energyFractions[j]));
		}

		//generate a neutrino energy on a power law (-2) distribution
		double rn3 = rand->Uniform(1);
		double eNu0 = std::pow(10.0,readConf->dNeutrinoEnergy);
		double eNu1 = std::pow(10.0,readConf->dNeutrinoEnergy+4);
		double eNu = std::pow((std::pow(eNu1,-1) - std::pow(eNu0,-1))*rn3 + std::pow(eNu0,-1),-1);
		mrsTau->SetNeutrinoEnergy(eNu);

		mrsTau->SetTauEnergyDistributionInverse(energyFractions,energyProbability);
		mrsTau->RunSimulation(chasmZN);

		// Calculate shower start position in CHASM coordinates
		double decayLength = mrsTau->GetDecayLength(); // Get decay length from tau simulation
		
		// Tau flight direction (unit vector) in CHASM coordinates
		double tau_dir_x = TMath::Sin(chasmZN) * TMath::Cos(showerAZ);
		double tau_dir_y = TMath::Sin(chasmZN) * TMath::Sin(showerAZ);
		double tau_dir_z = TMath::Cos(chasmZN);
		
		// Shower start position in CHASM coordinates (relative to emergence point)
		double shower_start_x = tau_dir_x * decayLength;
		double shower_start_y = tau_dir_y * decayLength;
		double shower_start_z = tau_dir_z * decayLength;
		
