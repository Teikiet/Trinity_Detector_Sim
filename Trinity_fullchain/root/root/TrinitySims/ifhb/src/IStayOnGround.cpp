		fIFHBOutText<<simPos[0]<<" ";
		fIFHBOutText<<simPos[1]<<" ";
		fIFHBOutText<<simPos[2]<<" ";
		fIFHBOutText<<readConf->fTelCollectionSphere<<" ";
