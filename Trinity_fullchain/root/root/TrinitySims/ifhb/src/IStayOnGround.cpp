		double showerEM = TMath::DegToRad()*rand->Uniform(0,15); //shower emergence angle in coordinate system of telescope, 1-5 deg
		double showerZN = TMath::Pi()/2 - showerEM; //shower zenith in coordinate system of telescope, 85-89 deg
		double showerAZ = TMath::DegToRad()*rand->Uniform(0,15); //shower azimuth, 0 deg
		//double showerAZ = TMath::ACos(rand->Uniform(TMath::Cos(TMath::DegToRad() * 15.0), TMath::Cos(TMath::DegToRad() * 0.0))); //shower azimuth, 0 deg
		if(rand->Uniform(1)>=0.5){showerAZ = 2*TMath::Pi() - showerAZ;} //randomly make azimuth negative
		double ppYdim = Constant::REarth*1000*(1-TMath::Sin(showerZN)); //y dimension of plane parallel to showerZN
		double ppXdim = 2*ppYdim*TMath::Tan(TMath::DegToRad()*readConf->fCameraAngularWidth); //x dimension of plane parallel to showerZN with width defined by fCameraAngularWidth
		//generate random point on triangular plane perpendicular to showerZN
		double rn1 = rand->Uniform(1);
		double rn2 = rand->Uniform(1);
		double rtX,rtY;
