int FindNuEnergyLine(std::string fileNameIn, double energy){
	int nLineEnergy = 0;
	bool isFound = false;
	std::vector<double> vFileContent;

	do{
		nLineEnergy = FileOps::FindToken(fileNameIn,"*",nLineEnergy);
		vFileContent = FileOps::RetrieveFileContent(fileNameIn,'h',nLineEnergy+1);
		if(vFileContent[0] == energy){
			isFound = true;
		}
		
		nLineEnergy++;

	}while(!isFound);

	return nLineEnergy;

}


